"""
RuanMei — Content performance learner.

Learns what works from ALL published posts (not just Stelle-generated ones),
tracks which patterns drive engagement, and surfaces insights as soft context
at the next generation.

Two data sources feed the observation history:
1. Stelle-generated posts: analyzed post-hoc after generation, scored when
   Ordinal sync brings back engagement metrics.
2. Ordinal analytics: all published posts are ingested, analyzed, and scored
   directly. This is the primary data pipeline — it captures every post the
   client has ever published, not just our output.

No hand-crafted archetypes. No taxonomy. The strategy space emerges from what
was actually written, described after the fact with open-ended LLM analysis.

Architecture:
- Open-ended LLM analysis extracts a free-text StrategyDescriptor from each post
- Descriptors feed into an observation history ranked by z-scored engagement
- Insights generated by LLM comparing top vs bottom performers' analyses
- Reward = z-scored composite per client's own distribution (no hand-tuned weights)

Storage: memory/{company}/ruan_mei_state.json

Usage:
    rm = RuanMei("runpod")

    # Ingest all published posts from Ordinal (primary data pipeline):
    await rm.ingest_from_ordinal(analytics_posts)

    # After Stelle generates a post (use same id as local_posts.id):
    descriptor = await rm.analyze_post(post_text)
    rm.record(post_hash, descriptor, post_body=text, local_post_id=draft_uuid)

    # After push to Ordinal:
    rm.link_ordinal_post_id(draft_uuid, ordinal_workspace_post_id)

    # Before next generation, get soft insight context:
    insights = await rm.generate_insights()
    # → "Posts with concrete numbers averaged 2.3x higher engagement..."

    # When Ordinal sync brings back metrics for pending posts:
    rm.update_by_ordinal_post_id(ordinal_id, metrics, posted_body=live_copy)  # preferred
    # or rm.update(post_hash, metrics, posted_body=live_copy) if no id linked
"""

import hashlib
import json
import logging
import math
from dataclasses import asdict, dataclass, field
from datetime import datetime, timezone
from difflib import SequenceMatcher
from typing import Optional

import anthropic

from backend.src.db import vortex as P

logger = logging.getLogger(__name__)

# Minimum observations before RuanMei starts generating insights.
MIN_OBSERVATIONS_FOR_INSIGHTS = 5

# How many recent posts to include in content state tracking.
RECENT_HISTORY_LENGTH = 10

# Percentile thresholds for top/bottom performer comparison.
TOP_PERCENTILE = 0.75
BOTTOM_PERCENTILE = 0.25

# How many recent posts to include in the content trajectory section.
TRAJECTORY_POSTS = 8


# ------------------------------------------------------------------
# Data classes
# ------------------------------------------------------------------

@dataclass
class StrategyDescriptor:
    """Open-ended description of a post's notable characteristics.

    No predefined dimensions. The LLM decides what's worth noting about
    the post's construction, style, and approach. This avoids forcing
    analysis along human-designed axes (hook type, tone, etc.) that may
    not capture what actually drives engagement.
    """
    analysis: str = ""
    char_count: int = 0


@dataclass
class ContentState:
    """Content calendar context at the time of posting."""
    posts_this_week: int = 0
    days_since_last_post: int = 0
    posting_streak: int = 0
    engagement_trend: float = 0.0
    avg_impressions_recent: float = 0.0
    total_observations: int = 0


@dataclass
class Observation:
    """A single post observation: what was written, how it performed."""
    post_hash: str
    descriptor: dict  # serialized StrategyDescriptor
    content_state: dict  # serialized ContentState
    reward: Optional[dict] = None  # {immediate, raw_metrics} or None if pending
    post_body: str = ""  # draft / pre-push copy (from generation)
    posted_body: str = ""  # live LinkedIn copy from analytics when it differs from draft
    posted_at: str = ""
    recorded_at: str = ""
    scored_at: str = ""  # timestamp of most recent engagement scoring
    status: str = "pending"  # pending | scored
    local_post_id: str = ""  # SQLite local_posts.id — links push → observation
    ordinal_post_id: str = ""  # Ordinal workspace post id after push — primary sync key
    icp_reward: Optional[float] = None  # ICP quality signal in [-1, 1]; None until engagers fetched
    linkedin_post_url: str = ""  # Live LinkedIn URL (populated from ordinal analytics payload)
    edit_similarity: float = -1.0  # SequenceMatcher ratio of draft vs published (-1 = not yet computed)
    # Post-hoc tags from observation_tagger (None until tagged).
    # Free-text, no predefined taxonomy — tagger produces whatever clusters naturally.
    topic_tag: Optional[str] = None           # subject matter (e.g., "regulatory compliance")
    source_segment_type: Optional[str] = None  # type of source moment (e.g., "specific compliance gap")
    format_tag: Optional[str] = None          # structural format (e.g., "case study")
    # Source tracking — populated at generation time when the strategy brief
    # pipeline creates posts with explicit source attribution. Null for posts
    # ingested from Ordinal analytics without draft provenance.
    source_transcript: Optional[str] = None   # filename of source transcript
    abm_target: Optional[str] = None          # company name if ABM-targeted


# ------------------------------------------------------------------
# Core class
# ------------------------------------------------------------------

class RuanMei:
    """
    Content performance learner.

    Does NOT prescribe strategy to Stelle. Observes what Stelle produces,
    learns which patterns drive engagement, and surfaces insights as
    soft context.
    """

    def __init__(self, company: str):
        self.company = company
        self._state = self._load()

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    async def analyze_post(self, post_text: str) -> StrategyDescriptor:
        """
        Open-ended post analysis. The LLM decides what's notable about
        the post's construction, not us. No predefined dimensions.
        """
        prompt = (
            "Analyze this LinkedIn post. What are the most notable "
            "characteristics of how it's written? Describe whatever stands "
            "out to you about its construction, style, approach, and content. "
            "Be specific and concrete. Reference actual phrases or structural "
            "choices from the post.\n\n"
            "Do NOT use generic marketing labels like 'thought leadership' or "
            "'engaging hook.' Describe the actual mechanics of what the author did.\n\n"
            f"Post:\n{post_text}\n\n"
            "Write 3-5 sentences of analysis. Plain text, no JSON, no bullet points."
        )

        try:
            client = anthropic.Anthropic()
            resp = client.messages.create(
                model="claude-opus-4-6",
                max_tokens=300,
                messages=[{"role": "user", "content": prompt}],
            )
            analysis = resp.content[0].text.strip()
            return StrategyDescriptor(
                analysis=analysis,
                char_count=len(post_text),
            )
        except Exception as e:
            logger.warning("[RuanMei] Post analysis failed: %s", e)
            return StrategyDescriptor(char_count=len(post_text))

    def record(
        self,
        post_hash: str,
        descriptor: StrategyDescriptor,
        content_state: Optional[ContentState] = None,
        post_body: str = "",
        local_post_id: str = "",
    ) -> None:
        """Record a generated post's strategy descriptor. Awaits reward from Ordinal.

        ``local_post_id`` should be the same id as ``local_posts.id`` so a later Ordinal
        push can attach ``ordinal_post_id`` without relying on unchanged body text.
        """
        if content_state is None:
            content_state = self.build_content_state()

        obs = Observation(
            post_hash=post_hash,
            descriptor=asdict(descriptor),
            content_state=asdict(content_state),
            post_body=post_body,
            posted_body="",
            recorded_at=_now(),
            status="pending",
            local_post_id=(local_post_id or "").strip(),
            ordinal_post_id="",
        )

        # Drop prior row for same draft id or same content hash.
        self._state["observations"] = [
            o
            for o in self._state["observations"]
            if (not obs.local_post_id or o.get("local_post_id") != obs.local_post_id)
            and o.get("post_hash") != post_hash
        ]
        self._state["observations"].append(asdict(obs))
        self._save()
        logger.info(
            "[RuanMei] Recorded post for %s (hash=%s local_id=%s)",
            self.company,
            post_hash,
            obs.local_post_id or "—",
        )

    def link_ordinal_post_id(self, local_post_id: str, ordinal_post_id: str) -> bool:
        """After a push, associate the Ordinal workspace id with the observation (any status)."""
        lid = (local_post_id or "").strip()
        oid = (ordinal_post_id or "").strip()
        if not lid or not oid:
            return False
        for obs in self._state["observations"]:
            if obs.get("local_post_id") == lid:
                obs["ordinal_post_id"] = oid
                self._save()
                logger.info("[RuanMei] Linked Ordinal id %s… to local draft %s", oid[:12], lid[:8])
                return True
        return False

    def update(
        self,
        post_hash: str,
        metrics: dict,
        posted_body: str = "",
        ordinal_post_id: str = "",
        linkedin_post_url: str = "",
    ) -> bool:
        """
        Score or re-score an observation with engagement metrics from Ordinal.
        Matches pending and already-scored observations so metrics stay fresh
        as impressions grow over time.  Also backfills ``ordinal_post_id`` and
        ``linkedin_post_url`` when provided (fixes observations created before
        these fields were wired).
        """
        for obs in self._state["observations"]:
            if obs.get("post_hash") == post_hash and obs.get("status") in ("pending", "scored"):
                reward = self._compute_reward(metrics)
                obs["reward"] = reward
                obs["status"] = "scored"
                obs["scored_at"] = _now()
                obs["posted_at"] = metrics.get("posted_at", obs.get("recorded_at", ""))
                if posted_body.strip():
                    obs["posted_body"] = posted_body.strip()
                    sim = _compute_edit_similarity(obs.get("post_body", ""), posted_body)
                    if sim >= 0:
                        obs["edit_similarity"] = round(sim, 4)
                if ordinal_post_id:
                    obs["ordinal_post_id"] = ordinal_post_id
                if linkedin_post_url:
                    obs["linkedin_post_url"] = linkedin_post_url
                self._save()
                logger.info(
                    "[RuanMei] Scored post for %s: immediate=%.3f (hash=%s)",
                    self.company, reward.get("immediate", 0), post_hash,
                )
                return True
        return False

    def update_by_ordinal_post_id(self, ordinal_post_id: str, metrics: dict, posted_body: str = "", linkedin_post_url: str = "") -> bool:
        """Match observation by Ordinal workspace id. Re-scores already-scored observations."""
        oid = (ordinal_post_id or "").strip()
        if not oid:
            return False
        for obs in self._state["observations"]:
            if obs.get("ordinal_post_id") == oid and obs.get("status") in ("pending", "scored"):
                reward = self._compute_reward(metrics)
                obs["reward"] = reward
                obs["status"] = "scored"
                obs["scored_at"] = _now()
                obs["posted_at"] = metrics.get("posted_at", obs.get("recorded_at", ""))
                if posted_body.strip():
                    obs["posted_body"] = posted_body.strip()
                    sim = _compute_edit_similarity(obs.get("post_body", ""), posted_body)
                    if sim >= 0:
                        obs["edit_similarity"] = round(sim, 4)
                if linkedin_post_url:
                    obs["linkedin_post_url"] = linkedin_post_url
                self._save()
                logger.info(
                    "[RuanMei] Scored post for %s: immediate=%.3f (ordinal_id=%s…)",
                    self.company,
                    reward.get("immediate", 0),
                    oid[:12],
                )
                return True
        return False

    def update_by_text(
        self,
        post_text: str,
        metrics: dict,
        ordinal_post_id: str = "",
        linkedin_post_url: str = "",
    ) -> bool:
        """
        Try to match a post by text hash and update it.
        Fallback when analytics payload has no Ordinal post id or id was not linked.
        Propagates ``ordinal_post_id`` and ``linkedin_post_url`` so they get
        backfilled on observations that were created before these fields existed.
        """
        post_hash = _hash(post_text)
        return self.update(
            post_hash, metrics,
            posted_body=post_text,
            ordinal_post_id=ordinal_post_id,
            linkedin_post_url=linkedin_post_url,
        )

    def update_icp_reward(
        self,
        ordinal_post_id: str,
        icp_score: float,
        linkedin_post_url: str = "",
        icp_segments: dict | None = None,
        icp_match_rate: float | None = None,
    ) -> bool:
        """Store ICP reward signal on a scored observation and recompute composite.

        Called by ordinal_sync after engager profiles are fetched and scored.
        Optionally stores segment breakdown for the ICP audience flywheel.
        Returns True when a matching observation was found and updated.
        """
        oid = (ordinal_post_id or "").strip()
        if not oid:
            return False
        for obs in self._state["observations"]:
            if obs.get("ordinal_post_id") != oid:
                continue
            if obs.get("status") != "scored":
                continue
            # Recompute reward blending in the ICP signal.
            existing_reward = obs.get("reward", {})
            raw = existing_reward.get("raw_metrics", {})
            if raw:
                obs["reward"] = self._compute_reward(raw, icp_score=icp_score)
            else:
                # No raw metrics stored (shouldn't happen, but be safe).
                obs["icp_reward"] = round(icp_score, 4)
            # Store segment breakdown for ICP flywheel
            if icp_segments:
                obs["icp_segments"] = icp_segments
            if icp_match_rate is not None:
                obs["icp_match_rate"] = round(icp_match_rate, 4)
            if linkedin_post_url:
                obs["linkedin_post_url"] = linkedin_post_url
            self._save()
            logger.info(
                "[RuanMei] ICP reward updated for %s: icp=%.4f match_rate=%s (ordinal_id=%s…)",
                self.company, icp_score,
                f"{icp_match_rate:.2%}" if icp_match_rate is not None else "N/A",
                oid[:12],
            )
            return True
        return False

    async def generate_insights(self) -> str:
        """
        Generate a natural-language insight summary for Stelle's context.

        Produces two sections:
        1. Performance patterns — what distinguishes top vs bottom performers.
        2. Content trajectory — recent posting sequence with engagement, letting
           the LLM identify series momentum, topic gaps, repetition, and rotation
           effects without predefined category taxonomies.

        Returns empty string if not enough data.
        """
        scored = [o for o in self._state["observations"] if o.get("status") == "scored"]

        if len(scored) < MIN_OBSERVATIONS_FOR_INSIGHTS:
            return ""

        # Split into top and bottom performers by immediate reward.
        scored_sorted = sorted(scored, key=lambda o: o.get("reward", {}).get("immediate", 0))
        n = len(scored_sorted)
        cutoff_top = int(n * TOP_PERCENTILE)
        cutoff_bottom = int(n * BOTTOM_PERCENTILE)

        top = scored_sorted[cutoff_top:]
        bottom = scored_sorted[:cutoff_bottom] if cutoff_bottom > 0 else scored_sorted[:1]

        # Format observations for the LLM.
        def _fmt(obs_list: list[dict]) -> str:
            lines = []
            for o in obs_list[-8:]:  # Cap at 8 to keep prompt reasonable.
                d = o.get("descriptor", {})
                r = o.get("reward", {})
                raw = r.get("raw_metrics", {})
                analysis = d.get("analysis", "No analysis available")
                lines.append(
                    f"[Impressions: {raw.get('impressions', '?')} | "
                    f"Comments: {raw.get('comments', '?')} | "
                    f"Reactions: {raw.get('reactions', '?')} | "
                    f"Score: {r.get('immediate', 0):.3f}]\n"
                    f"  Analysis: {analysis}"
                )
            return "\n\n".join(lines)

        prompt = (
            "You are analyzing LinkedIn post performance for a client. "
            "Compare the high-performing and low-performing posts below. "
            "What patterns distinguish the winners?\n\n"
            "Be specific and actionable. Reference actual numbers where possible. "
            "Write 3-5 sentences max. No marketing jargon. No bullet points. "
            "Speak as if you're giving a colleague a quick heads-up.\n\n"
            f"HIGH PERFORMERS (top 25%):\n{_fmt(top)}\n\n"
            f"LOW PERFORMERS (bottom 25%):\n{_fmt(bottom)}\n\n"
            f"Total posts analyzed: {n}"
        )

        # Add temporal patterns if we have enough data.
        temporal = self._compute_temporal_patterns(scored)
        if temporal:
            prompt += f"\n\nTEMPORAL DATA:\n{temporal}"

        edit_deltas = self._build_edit_delta_summary(scored)
        if edit_deltas:
            prompt += (
                "\n\n---\n\nDRAFT → LIVE EDIT PATTERNS (Amphoreus draft vs copy that actually ran on LinkedIn):\n"
                "Each draft-live pair includes an edit_similarity score (0=completely rewritten, 1=identical). "
                "Summarize what tends to change before posting and whether heavily-edited posts "
                "performed better or worse than lightly-edited ones. "
                "One concise paragraph. If examples are sparse, say so.\n\n"
                + edit_deltas
            )

        feedback = self._build_feedback_patterns()
        if feedback:
            prompt += (
                "\n\n---\n\nEDITORIAL FEEDBACK (recent notes from editors on drafts):\n"
                "Add a sentence or two noting any recurring editorial preferences "
                "or corrections that the generation system should learn from.\n\n"
                + feedback
            )

        # Layer 2: Content trajectory — pass recent post bodies chronologically so
        # the LLM can identify series momentum, topic saturation, rotation gaps, and
        # repetition without us pre-defining any categories.
        trajectory = self._build_content_trajectory(scored)
        if trajectory:
            prompt += (
                "\n\n---\n\nCONTENT TRAJECTORY (most recent posts, oldest → newest):\n"
                "After your performance analysis, add a second paragraph that describes "
                "what you observe about the sequence: are topics repeating? is there "
                "momentum from a series? what angles or themes are conspicuously absent "
                "given what has already been posted? Keep it to 2-3 sentences.\n\n"
                + trajectory
            )

        # Layer 3: ICP signal — how well each post attracted ideal-profile engagers.
        icp_summary = self._build_icp_summary(scored)
        if icp_summary:
            prompt += (
                "\n\n---\n\nICP ENGAGER SIGNAL (who actually engaged with these posts):\n"
                "Add a final sentence noting whether the posts that performed best "
                "also attracted the most on-profile (ICP) engagers, or if high-reach "
                "posts attracted off-profile audiences. Keep it one sentence.\n\n"
                + icp_summary
            )

        try:
            client = anthropic.Anthropic()
            resp = client.messages.create(
                model="claude-opus-4-6",
                max_tokens=700,
                messages=[{"role": "user", "content": prompt}],
            )
            return resp.content[0].text.strip()
        except Exception as e:
            logger.warning("[RuanMei] Insight generation failed: %s", e)
            return ""

    @classmethod
    def generate_cross_client_insights(cls) -> str:
        """Aggregate anonymized top-performing patterns across ALL clients.

        Intended as a cold-start fallback for new clients with < MIN_OBSERVATIONS_FOR_INSIGHTS.
        Reads every client's ruan_mei_state.json, collects top-25% scored observations,
        anonymizes them, and asks the LLM to summarize what universally drives LinkedIn engagement.
        """
        memory_root = P.MEMORY_ROOT
        if not memory_root.exists():
            return ""

        all_top: list[dict] = []
        for company_dir in memory_root.iterdir():
            if not company_dir.is_dir():
                continue
            state_path = company_dir / "ruan_mei_state.json"
            if not state_path.exists():
                continue
            try:
                state = json.loads(state_path.read_text(encoding="utf-8"))
            except Exception:
                continue
            scored = [o for o in state.get("observations", []) if o.get("status") == "scored"]
            if len(scored) < 5:
                continue
            scored.sort(key=lambda o: o.get("reward", {}).get("immediate", 0))
            cutoff = int(len(scored) * 0.75)
            top = scored[cutoff:]
            for o in top:
                all_top.append({
                    "analysis": o.get("descriptor", {}).get("analysis", ""),
                    "score": o.get("reward", {}).get("immediate", 0),
                    "impressions": o.get("reward", {}).get("raw_metrics", {}).get("impressions", 0),
                    "edit_similarity": o.get("edit_similarity", -1),
                })

        if len(all_top) < 10:
            return ""

        all_top.sort(key=lambda o: o["score"], reverse=True)
        sample = all_top[:20]

        lines = []
        for o in sample:
            sim_str = f"edit_sim={o['edit_similarity']:.2f}" if o["edit_similarity"] >= 0 else "edit_sim=N/A"
            lines.append(
                f"[Score: {o['score']:.3f} | Impressions: {o['impressions']} | {sim_str}]\n"
                f"  {o['analysis'][:400]}"
            )

        prompt = (
            "You are analyzing top-performing LinkedIn posts across multiple anonymous clients. "
            "What universal patterns make LinkedIn posts perform well? "
            "Be specific. 3-5 sentences. No marketing jargon. "
            "These insights will help a new client with no performance history yet.\n\n"
            f"TOP PERFORMERS (anonymized, {len(all_top)} total across all clients):\n\n"
            + "\n\n".join(lines)
        )

        try:
            client = anthropic.Anthropic()
            resp = client.messages.create(
                model="claude-opus-4-6",
                max_tokens=400,
                messages=[{"role": "user", "content": prompt}],
            )
            return resp.content[0].text.strip()
        except Exception as e:
            logger.warning("[RuanMei] Cross-client insight generation failed: %s", e)
            return ""

    def _build_content_trajectory(self, scored: list[dict]) -> str:
        """
        Build a chronological sequence of recent scored post bodies with their
        engagement scores, capped to avoid bloating the prompt.

        Only includes observations that have a stored post_body. Returns empty
        string if fewer than 3 posts have bodies (not enough for pattern detection).
        """
        # Sort chronologically by posted_at, fall back to recorded_at.
        def _ts(o: dict) -> str:
            return o.get("posted_at") or o.get("recorded_at") or ""

        chronological = sorted(
            [
                o
                for o in scored
                if (o.get("posted_body") or o.get("post_body") or "").strip()
            ],
            key=_ts,
        )

        if len(chronological) < 3:
            return ""

        # Take the most recent N posts; truncate each body to keep prompt manageable.
        recent = chronological[-TRAJECTORY_POSTS:]
        MAX_BODY_CHARS = 400

        lines = []
        for i, o in enumerate(recent, 1):
            r = o.get("reward", {})
            raw = r.get("raw_metrics", {})
            body = (o.get("posted_body") or o.get("post_body") or "").strip()
            if len(body) > MAX_BODY_CHARS:
                body = body[:MAX_BODY_CHARS] + "…"
            lines.append(
                f"[Post {i} | Score: {r.get('immediate', 0):.3f} | "
                f"Impressions: {raw.get('impressions', '?')} | "
                f"Comments: {raw.get('comments', '?')}]\n{body}"
            )

        return "\n\n".join(lines)

    def _build_icp_summary(self, scored: list[dict]) -> str:
        """One-line summary of ICP reward signal across scored posts, for Stelle context."""
        icp_obs = [
            o for o in scored
            if o.get("reward", {}).get("icp_reward") is not None
        ]
        if len(icp_obs) < 3:
            return ""

        icp_pairs = [
            (o.get("reward", {}).get("immediate", 0), o.get("reward", {}).get("icp_reward", 0))
            for o in icp_obs
        ]
        avg_icp = sum(p[1] for p in icp_pairs) / len(icp_pairs)
        # Correlation direction: do high-immediate posts also have high icp?
        high_perf = [p for p in icp_pairs if p[0] > 0]
        low_perf = [p for p in icp_pairs if p[0] <= 0]
        avg_icp_high = sum(p[1] for p in high_perf) / len(high_perf) if high_perf else 0
        avg_icp_low = sum(p[1] for p in low_perf) / len(low_perf) if low_perf else 0

        lines = [
            f"Posts with ICP data: {len(icp_obs)} | Average ICP score: {avg_icp:.3f}",
            f"High-performers avg ICP: {avg_icp_high:.3f} | Low-performers avg ICP: {avg_icp_low:.3f}",
        ]
        return "\n".join(lines)

    def _build_edit_delta_summary(self, scored: list[dict]) -> str:
        """Pairs of draft vs live copy with edit_similarity for posts where text differed."""
        chunks: list[str] = []
        for o in scored:
            before = (o.get("post_body") or "").strip()
            after = (o.get("posted_body") or "").strip()
            if not before or not after or before == after:
                continue
            sim = o.get("edit_similarity", -1)
            sim_label = f"edit_similarity={sim:.2f}" if sim >= 0 else "edit_similarity=N/A"
            r = o.get("reward", {})
            score_label = f"engagement_score={r.get('immediate', 0):.3f}"
            max_chars = 480
            b = before[:max_chars] + ("…" if len(before) > max_chars else "")
            a = after[:max_chars] + ("…" if len(after) > max_chars else "")
            chunks.append(
                f"--- DRAFT ({len(before)} chars) [{sim_label} | {score_label}] ---\n{b}\n"
                f"--- LIVE ({len(after)} chars) ---\n{a}"
            )
        if not chunks:
            return ""
        return "\n\n".join(chunks[-5:])

    def _build_feedback_patterns(self) -> str:
        """Read editorial feedback files and concatenate recent entries for insight context.

        Feedback lives in ``memory/{company}/feedback/`` as markdown or text files.
        Each file represents one round of editorial notes. We return the most recent
        entries (capped) so the LLM can detect patterns in how editors revise drafts.
        """
        fb_dir = P.feedback_dir(self.company)
        if not fb_dir.exists():
            return ""
        files = sorted(fb_dir.iterdir(), key=lambda f: f.stat().st_mtime, reverse=True)
        files = [f for f in files if f.suffix in (".md", ".txt", ".json")]
        if not files:
            return ""

        MAX_FILES = 5
        MAX_CHARS_PER_FILE = 600
        entries: list[str] = []
        for f in files[:MAX_FILES]:
            try:
                raw = f.read_text(encoding="utf-8", errors="replace").strip()
            except Exception:
                continue
            if not raw:
                continue
            snippet = raw[:MAX_CHARS_PER_FILE] + ("…" if len(raw) > MAX_CHARS_PER_FILE else "")
            entries.append(f"[{f.name}]\n{snippet}")

        if not entries:
            return ""
        return "\n\n".join(entries)

    def build_content_state(self) -> ContentState:
        """Build current content state from observation history."""
        scored = [o for o in self._state["observations"] if o.get("status") == "scored"]
        all_obs = self._state["observations"]

        if not all_obs:
            return ContentState()

        # Engagement trend: slope of immediate reward over last N scored posts.
        trend = 0.0
        recent_scored = scored[-RECENT_HISTORY_LENGTH:]
        if len(recent_scored) >= 3:
            rewards = [o.get("reward", {}).get("immediate", 0) for o in recent_scored]
            n = len(rewards)
            x_mean = (n - 1) / 2
            y_mean = sum(rewards) / n
            num = sum((i - x_mean) * (r - y_mean) for i, r in enumerate(rewards))
            den = sum((i - x_mean) ** 2 for i in range(n))
            trend = num / den if den > 0 else 0.0

        # Average impressions.
        impressions = [
            o.get("reward", {}).get("raw_metrics", {}).get("impressions", 0)
            for o in recent_scored if o.get("reward")
        ]
        avg_imp = sum(impressions) / len(impressions) if impressions else 0.0

        # Days since last post.
        days_since = 0
        if all_obs:
            last_ts = all_obs[-1].get("recorded_at", "")
            if last_ts:
                try:
                    last_dt = datetime.fromisoformat(last_ts.replace("Z", "+00:00"))
                    days_since = (datetime.now(timezone.utc) - last_dt).days
                except (ValueError, TypeError):
                    pass

        # Posts this week (last 7 days).
        now = datetime.now(timezone.utc)
        posts_this_week = 0
        for o in all_obs:
            ts = o.get("recorded_at", "")
            if ts:
                try:
                    dt = datetime.fromisoformat(ts.replace("Z", "+00:00"))
                    if (now - dt).days <= 7:
                        posts_this_week += 1
                except (ValueError, TypeError):
                    pass

        return ContentState(
            posts_this_week=posts_this_week,
            days_since_last_post=days_since,
            posting_streak=self._compute_streak(all_obs),
            engagement_trend=round(trend, 4),
            avg_impressions_recent=round(avg_imp, 1),
            total_observations=len(scored),
        )

    async def ingest_from_ordinal(
        self,
        posts: list[dict],
        batch_size: int = 50,
    ) -> int:
        """
        Ingest published posts from Ordinal analytics into the observation
        history. Each post gets analyzed and recorded as a scored observation
        so RuanMei learns from ALL client posts, not just Stelle-generated ones.

        Posts that already exist in the observation history (by text hash) are
        skipped to avoid duplicates.

        Args:
            posts: Raw post dicts from Ordinal analytics API.
            batch_size: Max posts to analyze per call (controls LLM cost).

        Returns number of posts successfully ingested.
        """
        existing_hashes = {
            o.get("post_hash") for o in self._state["observations"]
        }

        # Load draft map once — lets us distinguish Stelle-generated posts
        # (draft text available) from externally-authored posts (no draft).
        # When a draft exists, we populate post_body with the draft and
        # posted_body with the live analytics text, so edit_similarity
        # captures what the human changed before publishing. This is the
        # signal the feedback distiller uses to learn editorial preferences.
        draft_map = self._load_draft_map()

        ingested = 0
        for post in posts:
            if ingested >= batch_size:
                break

            text = (
                post.get("commentary")
                or post.get("text")
                or post.get("copy")
                or post.get("content")
                or post.get("post_text")
                or ""
            ).strip()
            if not text:
                continue

            post_hash = _hash(text)
            if post_hash in existing_hashes:
                continue

            impressions = post.get("impressionCount") or post.get("impressions") or 0
            reactions = post.get("likeCount") or post.get("reactions") or post.get("total_reactions") or 0
            comments = post.get("commentCount") or post.get("comments") or post.get("total_comments") or 0
            reposts = post.get("shareCount") or post.get("repostCount") or post.get("reposts") or 0

            if impressions == 0:
                continue

            posted_at = (
                post.get("publishedAt")
                or post.get("postedAt")
                or post.get("published_at")
                or ""
            )

            # Capture LinkedIn post URL for engager fetching.
            linkedin_url = (
                post.get("url")
                or post.get("linkedInUrl")
                or post.get("linkedin_url")
                or post.get("postUrl")
                or ""
            )

            # Capture Ordinal post id for linking.
            op = post.get("ordinalPost")
            ordinal_pid = ""
            if isinstance(op, dict):
                ordinal_pid = str(op.get("id") or "").strip()

            # Analyze the post (same as Stelle-generated posts).
            descriptor = await self.analyze_post(text)

            # Compute reward from metrics.
            metrics = {
                "impressions": impressions,
                "reactions": reactions,
                "comments": comments,
                "reposts": reposts,
                "posted_at": posted_at,
            }
            reward = self._compute_reward(metrics)

            # If this post came from Stelle (draft_map has an entry keyed by
            # its ordinal_post_id), the real draft lives in draft_map and the
            # analytics text is the LIVE copy. Otherwise it's externally
            # authored — no draft exists, and edit_similarity is meaningless.
            draft_text = ""
            if ordinal_pid and ordinal_pid in draft_map:
                draft_text = (draft_map[ordinal_pid].get("original_text") or "").strip()

            if draft_text:
                obs_post_body = draft_text
                obs_posted_body = text
                obs_edit_sim = _compute_edit_similarity(draft_text, text)
            else:
                obs_post_body = text
                obs_posted_body = ""
                obs_edit_sim = -1.0

            # Record as a fully scored observation.
            obs = Observation(
                post_hash=post_hash,
                descriptor=asdict(descriptor),
                content_state=asdict(self.build_content_state()),
                reward=reward,
                post_body=obs_post_body,
                posted_body=obs_posted_body,
                edit_similarity=round(obs_edit_sim, 4) if obs_edit_sim >= 0 else -1.0,
                posted_at=posted_at,
                recorded_at=_now(),
                status="scored",
                local_post_id="",
                ordinal_post_id=ordinal_pid,
                linkedin_post_url=linkedin_url,
            )

            self._state["observations"].append(asdict(obs))
            existing_hashes.add(post_hash)
            ingested += 1

        if ingested:
            self._save()
            logger.info(
                "[RuanMei] Ingested %d posts from Ordinal for %s",
                ingested, self.company,
            )

        return ingested

    def backfill_edit_similarity_from_draft_map(self) -> int:
        """Fix observations where post_body holds live text instead of the draft.

        Walks existing scored observations. For each one with an
        ``ordinal_post_id`` that maps to an entry in ``draft_map.json``,
        promotes the current ``post_body`` (which was set to live text by
        the pre-fix ``ingest_from_ordinal`` path) to ``posted_body`` and
        replaces ``post_body`` with the actual Stelle draft from draft_map.
        Recomputes ``edit_similarity`` on the corrected pair.

        Idempotent: skips observations where post_body already equals the
        draft text, or where the draft is missing/empty.

        Returns the number of observations modified.
        """
        draft_map = self._load_draft_map()
        if not draft_map:
            return 0

        modified = 0
        for obs in self._state.get("observations", []):
            oid = (obs.get("ordinal_post_id") or "").strip()
            if not oid or oid not in draft_map:
                continue
            if obs.get("status") != "scored":
                continue
            draft = (draft_map[oid].get("original_text") or "").strip()
            if not draft:
                continue
            current_pb = (obs.get("post_body") or "").strip()
            if current_pb == draft:
                continue  # already correct
            current_posted = (obs.get("posted_body") or "").strip()
            # The live text is whichever field currently holds the analytics
            # copy. Before the fix, ingest_from_ordinal stored it in post_body.
            live = current_posted or current_pb
            if not live:
                continue
            obs["post_body"] = draft
            obs["posted_body"] = live
            sim = _compute_edit_similarity(draft, live)
            obs["edit_similarity"] = round(sim, 4) if sim >= 0 else -1.0
            modified += 1

        if modified:
            self._save()
            logger.info(
                "[RuanMei] Backfilled edit_similarity for %d observations (%s)",
                modified, self.company,
            )

        return modified

